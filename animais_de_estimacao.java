package E_VERDADE;

public class animais_de_estimacao{
	
	private String nome;
	private  int idade;
	private String classe;
	
	public String getNome;
	public int getIdade;
	public String getClass;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getClasse() {
		return classe;
	}
	public void setClasse(String classe) {
		this.classe = classe;
	}
	

}
